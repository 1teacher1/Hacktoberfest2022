# Python string methods to manipulate strings 
mystring = "this is the string"

# To capitalize the first letter of the string
print(mystring.capitalize())

# To make the string in all uppercase
print(mystring.upper())

# To make the string in all lowercase
print(mystring.lower())

# To swap cases
print(mystring.swapcase())

# To convert the first character of each word to upper case
print(mystring.title())

# To return a tuple where the string is parted into three parts
print(mystring.partition('the'))

# To split the string at line breaks and returns a list
print(mystring.splitlines())

# Reverse string by indexing
print(mystring[::-1])